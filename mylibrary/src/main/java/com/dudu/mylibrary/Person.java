public class Person {
    public String aName;
    public boolean sex;
    public int age;

    public Person() {
    }

    public String getaName() {
        return aName;
    }

    public boolean isSex() {
        return sex;
    }

    public int getAge() {
        return age;
    }

    public void setaName(String aName) {
        this.aName = aName;
    }

    public void setSex(boolean sex) {
        this.sex = sex;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
